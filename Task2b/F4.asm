EXTRN NEW_LINE: NEAR

CSEG SEGMENT PARA PUBLIC 'CODE'
	ASSUME CS:CSEG
	
	DEC_UNSIGN PROC NEAR
		PUSH BP
		MOV BP, SP
		MOV BX, [BP + 4] ; DEVIDEND
		MOV AX, [BX] ; VALUE
		
		MOV DX, 'X'
		PUSH DX    ; STOP-SYMBOL
		
		NEXT:
		MOV DX, 0
		MOV CX, 10
		DIV CX     ; AX = AX / CX; DX = R(AX / CX)
		PUSH DX    ; PUSHING REMAINDER
		CMP AX, 0
		JNE NEXT
		
		PRINT:		
		POP DX
		CMP DX, 'X'
		JE FINISH
		MOV AH, 2
		ADD DX, '0'
		INT 21H
		JMP PRINT
		
		FINISH:
		CALL NEW_LINE
		
		POP BP
		RET 2
	DEC_UNSIGN ENDP
	
CSEG ENDS

PUBLIC DEC_UNSIGN

END